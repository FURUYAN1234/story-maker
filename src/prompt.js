import {
  THEME_RANDOM_BASE, THEME_MODIFIERS, THEME_ADJUNCTS,
} from './data.js';

const rnd = arr => arr[Math.floor(Math.random() * arr.length)];

/**
 * プロンプトの構築
 */
export function buildPrompt(s) {
  const baseMode = s.mode || '4koma';
  const genre = s.genreCustom || s.genre || 'コメディ';
  const theme = s.themeCustom || s.theme || 'ランダム';
  const worldview = s.worldviewCustom || s.worldview || '現代日本';
  const era = s.eraCustom || s.era || '現代';
  const target = s.targetCustom || s.target || '全年齢';
  const ending = s.endingCustom || s.ending || '意外な結末';
  const narr = s.narrCustom || s.narration || '三人称・客観';

  // キャラクター情報の構築
  let charDesc;
  if (!s.characters || s.characters.length === 0) {
    charDesc = '・未設定（AIが自由に2〜3人の個性的なキャラを設定すること）';
  } else {
    charDesc = s.characters.map((c, i) => {
      const name = c.name || `(AI命名:キャラ${i + 1})`;
      const role = c.role || '未定';
      const personality = c.personality || '未定';
      const detail = c.note ? ` [${c.note}]` : '';
      return `${i + 1}. ${name} (${role}) — 性格:${personality}${detail}`;
    }).join('\n');
  }

  // 長編小説指示は別途プロンプト分岐内で処理するため削除
  // 文字数指定
  const fmt = s.charCount ? `\n※ 指定文字数：約${s.charCount}文字程度` : '';
  const supplement = s.supplement ? `\n【追加指示】\n${s.supplement}` : '';

  // 出力モードのラベル生成
  const modeLabels = {
    '4koma': '4コマネタ', 'short_short': 'ショートショート', 'novel': '短編小説',
    'medium': '中編小説', 'long': '長編小説',
    'scenario': '脚本/台本', 'manga': 'ストーリー漫画', 'essay': 'エッセイ', 'poem': '詩・ポエム',
    'tale': '童話/絵本', 'letter': '手紙/書簡体', 'diary': '日記/独白体',
    'documentary': 'ドキュメンタリー', 'radio': 'ラジオドラマ'
  };
  const modeLabel = s.modeCustom || modeLabels[baseMode] || baseMode;

  // モード別文体ガイド（厨二病統一問題への対策）
  const modeStyleGuides = {
    '4koma': '\n\n【文体指定：4コマ漫画風】\n起承転結を4段階で明確にし、最後に笑いまたは意外性のあるオチをつけること。文体は軽快でテンポよく、深刻になりすぎないこと。セリフ主体で進行させること。暗い展開・壮大な伏線は原則不要。',
    'short_short': '\n\n【文体指定：ショートショート】\n短い中にキレのあるオチや余韻を残すこと。冗長な描写は避け、簡潔で鋭い文体を心がけること。星新一のような洗練された構成を意識すること。',
    'novel': '\n\n【文体指定：短編小説】\nキャラクターの内面描写と情景描写のバランスを取り、文学的な読み応えを出すこと。',
    'medium': '\n\n【文体指定：中編小説】\n複数のシーンや場面転換を含め、物語に厚みを持たせること。伏線と回収を意識した構成にすること。',
    'scenario': '\n\n【文体指定：脚本・台本】\nト書き（情景・動作指示）とセリフを明確に分離すること。映像的な演出を指示に含め、脚本フォーマットに準拠すること。',
    'manga': '\n\n【文体指定：ストーリー漫画原作】\nビジュアルで映える場面構成を意識し、セリフは簡潔に、動きのある描写を心がけること。コマ割りを意識した場面切り替えを行うこと。',
    'essay': '\n\n【文体指定：エッセイ】\n個人的な視点や体験を交え、読者に共感や気づきを与える柔らかな文体にすること。劇的な展開・伏線・「封印」「覚醒」「運命」のような大仰な要素は厳禁。日常の機微を丁寧に拾い、肩の力を抜いた語り口にすること。',
    'poem': '\n\n【文体指定：詩・ポエム】\nリズムと韻律を意識し、言葉の響きと余白を大切にすること。散文的にならず、詩的な飛躍を恐れないこと。',
    'fairy': '\n\n【文体指定：童話・絵本】\n平易で温かみのある言葉遣いを使い、教訓や希望を込めること。暗すぎる展開は避け、子供にも読み聞かせられるトーンを保つこと。',
    'letter': '\n\n【文体指定：手紙・書簡体】\n特定の相手に語りかける形式を守ること。日付や挨拶文も自然に含め、人間味のある文体にすること。',
    'diary': '\n\n【文体指定：日記・独白体】\n書き手の内面を生々しく描くこと。整理されすぎない思考の流れを意識し、独り言のような率直さを大切にすること。',
    'documentary': '\n\n【文体指定：ドキュメンタリー】\n客観的な視点を基調としつつ、取材対象への共感を込めた文体にすること。事実の積み重ねで説得力を出すこと。',
    'radio': '\n\n【文体指定：ラジオドラマ】\n音声のみで伝わるよう、効果音指示（SE:）・BGM指示を含め、セリフとナレーションで場面を描くこと。',
  };
  const styleGuide = modeStyleGuides[baseMode] || '';

  // 時代考証ルール（非現代の場合のみ追加）
  const eraRule = (era && !['現代', 'ランダム', ''].includes(era))
    ? '\n\n【時代考証ルール（厳守）】\n・時代設定「' + era + '」の語彙・風俗・技術水準を厳守すること。\n・その時代に存在しない概念・道具・言い回しを使わないこと（例：大正時代に「スマホ」、江戸時代に「電話」等）。\n・登場人物の詳細メモに時代不適合な現代表現があっても、時代に即した表現に自動で読み替えること（例：「スポーツマン体型」→「鍛え抜かれた体躯」）。\n・ただし、タイムスリップ等の時代錯誤がテーマ・世界観で意図されている場合はこの限りではない。'
    : '';

  // プロンプト本文
  let prompt = '';
  if (baseMode === 'long') {
    prompt = `# 厳格なシステム命令
あなたは「プロンプトエンジニア」です。小説家ではありません。
絶対に物語の本文を執筆しないでください！

以下の【ユーザー指定設定】を元に、別のLLMに長編小説を分割で執筆させるための「マスター指示書（プロンプト）」を作成してください。
出力はマークダウンのコードブロック(\`\`\`)のみとし、あなた自身の挨拶や返答、物語の本文は一切不要です。

【ユーザー指定設定】
・ジャンル: ${genre}
・テーマ: ${theme}
・時代: ${era}
・世界観・雰囲気: ${worldview}
・語り口: ${narr}
・ターゲット層: ${target}
・結末の方向性: ${ending}
・登場人物: ${charDesc}
${fmt}${supplement}

---
# 必須出力フォーマット（雛形に沿って埋めること）
\`\`\`markdown
あなたはプロのベストセラー小説家です。これから私と協力して、数万〜数十万文字におよぶ長編小説を1章ずつ分割で執筆します。

## 【物語の基本設定】
（※ ここにジャンル、時代、世界観などの詳細を小説家向けに深く描写して記載）

## 【登場人物の深掘り】
（※ 登場人物の裏設定、動機、関係性、口調などを詳細に記載）

## 【プロット（全章構成）】
（※非常に重要：APIの出力上限で途切れるのを防ぐため、必ず各章のあらすじは1〜2行で極めて簡潔にまとめ、プロンプト全体が長大にならないよう強く制限してください）

## 【AIっぽさ排除の厳守ルール】
・「最適化」「本質的」「羅針盤」「〜を実現します」などの抽象語、ビジネス用語、陳腐な比喩を一切禁止。
・「まとめると」「いかがでしたか？」などのブログ的末尾は禁止。
・「です」「ます」の単調な連続を避け、体言止めや感情表現を交えリズム良く。

## 【対話型エージェントとしての振舞い（最重要）】
1. あなたは1度に1つの章しか執筆してはいけません。
2. 章を書き終えるごとに、必ず『第◯章の執筆が完了しました。続けて次の章を執筆しますか？』と私に尋ね、私が『はい』や『続きをお願い』と簡単に答えるまで、絶対に次の章を勝手に書き始めないでください。
3. **【文脈維持メモ（内部ログ）】** 長編執筆における設定崩壊や記憶忘れを防ぐため、毎回、章の終わりに（私への質問の直前に）、短く簡潔に以下のメモを残してください：
   - 【回収待ち伏線メモ】現在残っている謎や伏線
   - 【次章の展開予定】次章で何を書くかの簡単な備忘録
4. **章の終わりは上記のメモと質問のみとし、自己分析、結論、根拠などの不要なメタデータは一切出力しないでください。**

## 【初回のあなた（AI）のアクション（第1章の出力）】
プロンプトを受け取ったら、まずは読者が全体像を把握できるよう、以下の【作品ヘッダー情報】を出力し、その直下から**そのまま第1章の本文を書き始めて**ください。

【作品ヘッダー情報】（※このヘッダーは第1章の最初のみ出力し、第2章以降は出力しないでください）
*   **タイトル**：（魅力的なタイトルを考案）
*   **予定文字数**：（例：約15万字）
*   **全構成**：（例：全12章構成）
*   **あらすじ**：（物語の概要を3〜4行で簡潔に）

---

さあ、上記の全指示と設定を読み込みましたか？
理解した場合は、まず【作品ヘッダー情報】を出力し、続けて「第1章」の本文を圧倒的な熱量と描写力で数千文字規模で執筆してください。文字数や描写の出し惜しみは厳禁です。
最後には必ず上記ルールの通り「文脈維持メモ」を残し、「第1章の執筆が完了しました。続けて第2章を執筆しますか？」と尋ねて待機してください。
\`\`\`
`;
  } else {
    prompt = `あなたはプロの書き手です。以下の詳細設定に基づき、読む人の心を動かす「${modeLabel}」を執筆してください。

【AIっぽさを完全に排除するための厳守ルール】
以下の「AI特有の癖」を完全に排除し、人間が書いたような血の通った自然な文章にしてください。
1. 語彙・表現：
   - 「最適化」「本質的」「効果的」「重要」「〜を実現します」などの抽象的なビジネス用語や万能語は禁止。具体的な動詞や情景描写（五感や感情）に言い換えること。
   - 「羅針盤」「土台」「エンジン」「航海」などの既視感のある陳腐な比喩は避け、比喩に頼らず言い切ること。
   - 「これにより」「また」「さらに」「したがって」などの不要な接続詞を乱用せず、文脈で自然に繋ぐこと。
2. リズム構成・定形句の排除：
   - 「〜です。」「〜ます。」と同じ長さ・語尾の文を連続して使わない。体言止めや「〜でしょう」「〜かもしれません」「〜ではないでしょうか」などを意図的に混ぜ、短文と長文でリズムに緩急をつけること。
   - 「結論から言うと」「本記事では」「以下で解説します」などの説明書のような前置き宣言は一切禁止。
   - 文章の最後に「まとめると」「結論として」「要するに」と再掲・要約しない。読者への「いかがでしたか？」等の問いかけや「参考になれば幸いです」といった締めの言葉も一切不要。余韻を残して自然に終わらせること。
3. 事なかれ主義・逃避の禁止：
   - 「一般的に」「多くの場合」「一概には言えませんが」「状況によりますが」といった安全クッション（保険）や両論併記は徹底排除し、世界観に沿って堂々と言い切ること。
4. 記号・装飾：
   - 短い文章での過度な箇条書き、見出し記号（#）の乱用を避けること。
   - アスタリスク（*）、過度な「」（カギカッコ）や（）（カッコ書き）、コロン（：）、スラッシュ（／）の多用を禁止し、文脈に自然に溶け込ませること。

【基本設定】
・ジャンル: ${genre}
・テーマ: ${theme}
・時代: ${era}
・世界観・雰囲気: ${worldview}
・語り口: ${narr}
・ターゲット層: ${target}
・結末の方向性: ${ending}

【登場人物】
${charDesc}

【執筆ルール（最重要）】
1. 予測可能な展開を意図的に回避し、読者を驚かせること。
2. キャラクターは設定された性格から生まれる固有の反応をすること。
3. 情景描写と心理描写のバランスを取り、臨場感のある文章にすること。
4. 「${ending}」という結末に向かって、伏線を自然に配置すること。
5. 登場人物が複数の場合、互いの関係性（協力、対立、秘密の共有など）を意識すること。${eraRule}${styleGuide}${fmt}${supplement}

【出力形式】
※ 禁止：タイトルに「」や【】などの装飾記号はAI側で付与せず、テキストのみで出力すること。
1行目に「タイトル: ○○」の形式で物語のタイトルを出力すること。
2行目は空行。
3行目から本文を開始してください。
物語が完全に終了した際は、最後に必ず「【完】」（続く場合は「【続く】」）と記載し、文章が途切れていないことを示してください。`;
  }

  // タグ生成
  const tags = [genre, era, worldview, target, ending, modeLabel];
  if (s.charCount) tags.push(`${s.charCount}字`);

  return { prompt, tags };
}

/**
 * ランダムテーマ生成
 */
export function generateRandomTheme() {
  let base = rnd(THEME_RANDOM_BASE);
  if (Math.random() < 0.55) base += ' ' + rnd(THEME_MODIFIERS);
  if (Math.random() < 0.35) base += ' ' + rnd(THEME_ADJUNCTS);
  return base;
}
